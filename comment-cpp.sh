comment.cpp.box()
{
	file="$*"
	echo |\
	sed   \
	-e a"${file:-x}"                        \
	-e ':b;s/^.\{78\}$/&/;te;s/$/@/;tb;:e'  \
	-e 's=.*=&\n&\n&='                     |\
	sed   \
	-e '1{ s/@/ /g; s/\(.\{75\}\).*/@@\1@/;}' \
	-e '2s=.=/='                              \
	-e '3s=.$=/='                             \
	-e '${s/.*/|&|/}'                         \
	-e 's/ /|/g'                              \
	-e '${:a; s/^.\{1,78\}$/ & /;ta}'         \
	-e '$s/ /-/g'                             \
	-e '$s/.../@@ /'                          \
	-e '$s/\(^.\{76\}\).*/\1 @/'              \
	-e '1h;3H'                                \
	-e '3d'                                   \
	-e '1h;2G'                                \
	-e '$G'                                   \
	-e '1d'                                   \
	-e 's/|/ /g'                              \
	-e 's=@=/=g'
}

